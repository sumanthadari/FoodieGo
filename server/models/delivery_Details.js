//import mongoose to create mongoose model
const mongoose = require("mongoose");

//create Schema
const delivery_DetailsSchema = new mongoose.Schema({
  firstName: {
    type: String,
    required: true,
  },
  lastName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true, // Ensures each email is unique
  },
  address: {
    street: {
      type: String,
      required: true,
    },
    city: {
      type: String,
      required: true,
    },
    zipCode: {
      type: String,
      required: true,
    },
    state: {
      type: String,
      required: true,
    },
    country: {
      type: String,
      required: true,
    },
  },
  phone: {
    type: String,
    required: true,
  },
});

//export this Schema
module.exports = mongoose.model("delivery_Details", delivery_DetailsSchema);
