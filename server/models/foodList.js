//import mongoose to create mongoose model
const mongoose = require("mongoose");

//create Schema
const menuSchema = new mongoose.Schema({
  id: {
    type: String,
    required: true,
    unique: true,
  },
  name: {
    type: String,
    required: true,
  },
  image: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  category: {
    type: String,
    required: true,
  },
});

//export this Schema
module.exports = mongoose.model("foodList", menuSchema);
